jumlah_buku = 10
print('Ibu berkata "Baca bukumu anjeng"')

jumlah_buku_sudah_dibaca_dan_dipahami = 0
total_jumlah_baca = 0

print(f"Jumlah buku yang sudah dibaca dan dipahami sebanyak {jumlah_buku_sudah_dibaca_dan_dipahami}")

while total_jumlah_baca < jumlah_buku:
    total_jumlah_baca == total_jumlah_baca * 2
    if jumlah_buku_sudah_dibaca_dan_dipahami == 9:
        print(f"Buku ke {jumlah_buku_sudah_dibaca_dan_dipahami + 1} belum paham")

    else:
        jumlah_buku_sudah_dibaca_dan_dipahami = jumlah_buku_sudah_dibaca_dan_dipahami + 1
        print(f"Buku ke {jumlah_buku_sudah_dibaca_dan_dipahami} sudah dibaca dan dipahami")




print(f"Saya sudah baca {jumlah_buku_sudah_dibaca_dan_dipahami} buku njenngg")