jumlah_buku = 89
print('Ibu berkata "Baca bukumu anjeng"')

jumlah_buku_sudah_dibaca = 0
print(f"Jumlah buku yang sudah dibaca sebanyak {jumlah_buku_sudah_dibaca}")

while jumlah_buku_sudah_dibaca < jumlah_buku:
    jumlah_buku_sudah_dibaca = jumlah_buku_sudah_dibaca + 1
    print(f"Buku ke {jumlah_buku_sudah_dibaca} sudah dibaca")
print(f"Saya sudah baca {jumlah_buku_sudah_dibaca} buku njenngg")